document.addEventListener('DOMContentLoaded', function () {
    const detailButtons = document.querySelectorAll('.detail-description-btn');

    detailButtons.forEach(button => {
        button.addEventListener('click', function (event) {
            event.preventDefault();

            const placeDetails = this.closest('.place-details');
            const detailsContainer = placeDetails.querySelector('.detailed-info-container');

            if (detailsContainer.style.display === 'block') {
                detailsContainer.style.display = 'none';
                this.textContent = 'Детальний опис';
            } else {
                const cityName = placeDetails.querySelector('h3').textContent;
                const countryName = this.getAttribute('data-country');
                detailsContainer.innerHTML = '';
                const cityInfo = getCityInfo(cityName);

                let infoIndex = 0;

                if (cityInfo.sections.length > 0) {
                    do {
                        const section = cityInfo.sections[infoIndex];

                        const sectionDiv = document.createElement('div');
                        sectionDiv.className = 'detail-section';

                        const sectionTitle = document.createElement('h4');
                        sectionTitle.textContent = section.title;
                        sectionDiv.appendChild(sectionTitle);

                        const sectionContent = document.createElement('p');
                        sectionContent.textContent = section.content;
                        sectionDiv.appendChild(sectionContent);

                        detailsContainer.appendChild(sectionDiv);

                        infoIndex++;
                    } while (infoIndex < cityInfo.sections.length);
                }

                detailsContainer.style.display = 'block';
                this.textContent = 'Сховати детальний опис';
            }
        });
    });
});

